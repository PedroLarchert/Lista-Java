package Exercicio1;

public class Disciplina {

	 private String nome;
	 private String horario;

	 public Disciplina(String nome, String horario) {
	     this.nome = nome;
	     this.horario = horario;
	 }

	 public String getNome() {
	     return nome;
	 }

	 public String getHorario() {
	     return horario;
	 }
	    
}
